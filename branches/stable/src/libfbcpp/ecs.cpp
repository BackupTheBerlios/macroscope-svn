//---------------------------------------------------------------------------
EClientServer::EClientServer(const ISC_STATUS_ARRAY stat, const utf8::String what)
  : ksys::Exception(-1, what)
{
  ISC_STATUS_ARRAY status;
  if( status != NULL ){
    codes_.resize(0);
    whats_.resize(0);
    char error[1024];
    if( api.fb_interpret != NULL ){
      const ISC_STATUS * pStatus = status, * aStatus = pStatus;
      while( api.fb_interpret(error,sizeof(error),&pStatus) != 0 ){
        if( *aStatus == 1 ){
          utf8::String e(strErrorHandler(aStatus[1]));
          if( e.strlen() > 0 ){
            strcat(error, ", isc_");
            strcat(error, e.c_str());
          }
        }
        codes_.add(aStatus[1]);
        whats_.add(error);
        aStatus = pStatus;
      }
    }
    else {
      ISC_STATUS * pStatus = status, * aStatus = pStatus;
      while( api.isc_interprete(error,&pStatus) != 0 ){
        if( *aStatus == 1 ){
          utf8::String e(strErrorHandler(aStatus[1]));
          if( e.strlen() > 0 ){
            strcat(error, ", isc_");
            strcat(error, e.c_str());
          }
        }
        codes_.add(aStatus[1]);
        whats_.add(error);
        aStatus = pStatus;
      }
    }
  }
}
//---------------------------------------------------------------------------
bool EClientServer::isFatalError() const
{
  for( intptr_t i = codes_.count() - 1; i >= 0; i-- )
    switch( codes_[i] ){
      case isc_network_error          :
      case isc_net_read_err           :
      case isc_net_write_err          :
      case isc_lost_db_connection     :
        return true;
    }
  return false;
}
