//---------------------------------------------------------------------------
utf8::String strErrorHandler(int32_t err)
{
  ISCErrorDesc  bs;
  bs.code_ = err;
  intptr_t  c = sizeof(iscErrors) / sizeof(iscErrors[0]);
  uintptr_t i = ksys::bSearch<ISCErrorDesc>(iscErrors, bs, c);
  if( c == 0 ) return iscErrors[i].error_;
  return utf8::String();
}
